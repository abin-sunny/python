d={2:"python",1:"cpp",3:"java"}
print("dictionary sorted in ascending order:",sorted(d.items()))
print("dictionary sorted in descending order:",sorted(d.items(),reverse=True))
print("keys sorted in ascending order:",sorted(d.keys()))
print("values sorted in ascending order:",sorted(d.values()))
print("keys sorted in descending order:",sorted(d.keys(),reverse=True ))
print("values sorted in decending order:",sorted(d.values(),reverse=True))



